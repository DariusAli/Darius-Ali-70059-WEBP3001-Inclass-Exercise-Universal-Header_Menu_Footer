        <h3>Purpose</h3>
        <p>
            &nbsp; &nbsp; &nbsp; This project deals with the development of an inventory management system for a single,
            mid-sized, supermarket. The system currently in use is paper based and tedious to track and
            account for goods. The supermarket wishes to automate the task by implementing a desktop
            application to aid the workers in their job and eliminate the paper-based system. The
            supermarket has many supplies which it needs to track; examples of such items are:
        </p>
        <ol>
            <li>Dry Goods</li>
            <li>Meats</li>
            <li>Vegetables</li>
            <li>Cleaning Products</li>
        </ol>
        <p>
            &nbsp; &nbsp; &nbsp; The supermarket warehouse department has a varied number of staff each with designated
            roles. The roles which are present in the warehouse are:
        </p>
        <ol>
            <li>Warehouse Manager</li>
            <li>Product Clerk</li>
            <li>Waerhouse Attendant</li>
        </ol>
        <p>
            &nbsp; &nbsp; &nbsp; The warehouse receives goods from various suppliers and it is the job of the Product Clerk to
            place the orders for certain stock items. The clerks would receive the items once they are
            delivered and enter them into the inventory system. The warehouse attendants are responsible for
            guiding the suppliers to the storage location of their product if or when they need to return said
            product. The warehouse manager keeps track of all purchases, deliveries and returns.
        </p>
        <h3>Documentation Conventions</h3>
